    private void bcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcalcularActionPerformed
        String ene = eN.getText();
        String eme = eM.getText();
        int n = 0;
        int m = 0;
        try{
            n = Integer.parseInt(ene);
            
            
        }
        catch(Exception e){
            eN.setText("No ingresaste un número entero N");
        }
        try{
            m = Integer.parseInt(eme);
            
        }
        catch(Exception e){
            eM.setText("No ingresaste un número entero M");
        }
        if ((n >= 1 && n <= 99) && (m >= 1 && m <= 99) ){
            String texto = "";
            int cont = 0;
            String nm = Integer.toString(n*m);
            int longitud = nm.length();
            int sum = sumadig(nm, longitud-1);
            int factor = factorial(n);
            
            int potenci = potencia(n,m);
            boolean r = primo(m,m-1);
            if (r) lprimo.setText("Sí");
            else lprimo.setText("No");
            mostrar(factor,sum,potenci);
            
            while (n*m >= fibonacci(cont)){
                texto += fibonacci(cont) + " ";
                
                cont++;
            }
            lfibonacci.setText(texto);
            
                
        } else if ((n < 1 || n > 99) ||(m < 1 || m > 99) ){
         lerror.setText("No ingresaste un número entre 1 y 99");
        }
        
        
        /*fibonacci(n);*/
    }//GEN-LAST:event_bcalcularActionPerformed

