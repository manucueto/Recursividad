    public int factorial(int numero){
        
        
        if (numero == 1){
            return 1;
        } else{
            return numero*factorial(numero-1);
        }
        
    }
    public void mostrar(int factorial,int suma,int poten){
        String rfactorial;
        String rsuma;
        String rpoten;
        rfactorial = Integer.toString(factorial);
        rsuma = Integer.toString(suma);
        rpoten = Integer.toString(poten);
        lfactorialN.setText(rfactorial);
        lsumadig.setText(rsuma);
        lpotencia.setText(rpoten);
        
    }
    public int sumadig(String nm, int longi){
        if(longi == 0){
            return Character.getNumericValue(nm.charAt(longi));
        }else{
            return  Character.getNumericValue(nm.charAt(longi)) + sumadig(nm,longi-1);
        }
        
    }
    
    public int fibonacci(int n){
       if (n == 0){
           return 0;
       }else if(n == 1 || n == 2) {
           return 1;
       }else{
           return (fibonacci(n-1)+fibonacci(n-2));
       }
    }
    
    public int potencia(int n,int m){
        if(m == 1){
            return n;
        }
        else{
            return n * potencia(n,m-1);
        }
    }
    public boolean primo(int m, int divi){
        if (m == 1){
            return true;
        }
        if(divi ==  1){
            return true;
        }else {
            if((int) m % divi == 0){
                return false;
            } else{
                return primo(m,divi-1);
            }
        }
        
    
    }
    
    
                    
        
        
    
